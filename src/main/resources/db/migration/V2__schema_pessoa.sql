-- =========================================
-- VIEWS (HIERARQUIA)
-- =========================================
CREATE VIEW vw_grupo_hierarquia_desc AS
WITH RECURSIVE arvore AS (
    SELECT id_grupo_pai,id_grupo_filho,1 nivel
    FROM pessoa_grupo_hierarquia
    UNION ALL
    SELECT a.id_grupo_pai,h.id_grupo_filho,a.nivel+1
    FROM pessoa_grupo_hierarquia h
    JOIN arvore a ON a.id_grupo_filho = h.id_grupo_pai
)
SELECT * FROM arvore;

CREATE VIEW vw_grupo_membros_ativos AS
SELECT *
FROM pessoa_grupo_membros
WHERE data_saida IS NULL;
