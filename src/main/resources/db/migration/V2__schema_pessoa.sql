-- ENDERECO
CREATE TABLE contato_endereco (
    id BIGINT PRIMARY KEY,
    cep VARCHAR(10),
    logradouro VARCHAR(255),
    numero VARCHAR(20),
    complemento VARCHAR(255),
    bairro VARCHAR(255),
    referencia TEXT,
    id_local BIGINT,
    latitude NUMERIC(10,7),
    longitude NUMERIC(10,7),
    geometria GEOMETRY,
    hash_endereco BYTEA,

    FOREIGN KEY (id) REFERENCES contato(id) ON DELETE CASCADE,
    FOREIGN KEY (id_local) REFERENCES local(id)
);

CREATE OR REPLACE FUNCTION normalizar_texto(t TEXT)
RETURNS TEXT AS $$ BEGIN RETURN lower(trim(coalesce(t,''))); END; $$ LANGUAGE plpgsql IMMUTABLE;

CREATE OR REPLACE FUNCTION trg_hash_endereco()
RETURNS TRIGGER AS $$
BEGIN
    NEW.hash_endereco :=
        digest(
            normalizar_texto(NEW.cep) ||
            normalizar_texto(NEW.logradouro) ||
            normalizar_texto(NEW.numero) ||
            normalizar_texto(NEW.complemento) ||
            normalizar_texto(NEW.bairro) ||
            coalesce(NEW.id_local::text,''),
            'sha256'
        );
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_hash_endereco
BEFORE INSERT OR UPDATE ON contato_endereco
FOR EACH ROW EXECUTE FUNCTION trg_hash_endereco();

CREATE UNIQUE INDEX uq_endereco_hash ON contato_endereco(hash_endereco);

