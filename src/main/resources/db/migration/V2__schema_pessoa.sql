-- VIEWS (HIERARQUIA)
-- =========================================
CREATE OR REPLACE VIEW pessoa.vw_grupo_hierarquia_desc AS
WITH RECURSIVE arvore AS (
    SELECT
        h.id_grupo_pai,
        h.id_grupo_filho,
        1 AS nivel,
        ARRAY[h.id_grupo_pai, h.id_grupo_filho] AS caminho
    FROM pessoa.pessoa_grupo_hierarquia h

    UNION ALL

    SELECT
        a.id_grupo_pai,
        h.id_grupo_filho,
        a.nivel + 1,
        a.caminho || h.id_grupo_filho
    FROM pessoa.pessoa_grupo_hierarquia h
    JOIN arvore a
        ON a.id_grupo_filho = h.id_grupo_pai
)
SELECT * FROM arvore;

CREATE OR REPLACE VIEW pessoa.vw_grupo_hierarquia_asc AS
WITH RECURSIVE arvore AS (
    SELECT
        h.id_grupo_pai,
        h.id_grupo_filho,
        1 AS nivel
    FROM pessoa.pessoa_grupo_hierarquia h

    UNION ALL

    SELECT
        h.id_grupo_pai,
        a.id_grupo_filho,
        a.nivel + 1
    FROM pessoa.pessoa_grupo_hierarquia h
    JOIN arvore a
        ON h.id_grupo_filho = a.id_grupo_pai
)
SELECT * FROM arvore;

CREATE VIEW vw_grupo_membros_ativos AS
SELECT *
FROM pessoa_grupo_membros
WHERE data_saida IS NULL;


CREATE OR REPLACE VIEW pessoa.vw_grupo_membros_hierarquia AS
WITH RECURSIVE arvore AS (
    SELECT
        g.id AS id_grupo_raiz,
        g.id AS id_grupo
    FROM pessoa.pessoa_grupo g

    UNION ALL

    SELECT
        a.id_grupo_raiz,
        h.id_grupo_filho
    FROM pessoa.pessoa_grupo_hierarquia h
    JOIN arvore a ON a.id_grupo = h.id_grupo_pai
)
SELECT
    a.id_grupo_raiz AS id_grupo,
    gm.id_pessoa,
    gm.papel,
    gm.descricao_funcao
FROM arvore a
JOIN pessoa.pessoa_grupo_membros gm
    ON gm.id_grupo = a.id_grupo
WHERE gm.data_saida IS NULL;

CREATE OR REPLACE VIEW pessoa.vw_grupo_hierarquia_nomeada AS
SELECT
    h.id_grupo_pai,
    p1.nome AS nome_grupo_pai,

    h.id_grupo_filho,
    p2.nome AS nome_grupo_filho,

    h.nome_pai_para_filho,
    h.nome_filho_para_pai
FROM pessoa.pessoa_grupo_hierarquia h
JOIN pessoa.pessoa p1 ON p1.id = h.id_grupo_pai
JOIN pessoa.pessoa p2 ON p2.id = h.id_grupo_filho;

CREATE OR REPLACE VIEW pessoa.vw_grupo_caminho AS
WITH RECURSIVE arvore AS (
    SELECT
        h.id_grupo_pai,
        h.id_grupo_filho,
        p1.nome::TEXT || ' > ' || p2.nome::TEXT AS caminho
    FROM pessoa.pessoa_grupo_hierarquia h
    JOIN pessoa.pessoa p1 ON p1.id = h.id_grupo_pai
    JOIN pessoa.pessoa p2 ON p2.id = h.id_grupo_filho

    UNION ALL

    SELECT
        a.id_grupo_pai,
        h.id_grupo_filho,
        a.caminho || ' > ' || p.nome
    FROM pessoa.pessoa_grupo_hierarquia h
    JOIN arvore a ON a.id_grupo_filho = h.id_grupo_pai
    JOIN pessoa.pessoa p ON p.id = h.id_grupo_filho
)
SELECT * FROM arvore;
