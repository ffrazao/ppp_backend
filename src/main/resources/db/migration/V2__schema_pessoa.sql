-- =========================================
-- SCHEMA
-- =========================================
CREATE SCHEMA IF NOT EXISTS pessoa;
SET search_path TO pessoa;

-- =========================================
-- EXTENSÕES
-- =========================================
CREATE EXTENSION IF NOT EXISTS postgis;
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- =========================================
-- ENUMS
-- =========================================
CREATE TYPE tipo_pessoa_enum AS ENUM ('fisica','juridica','grupo');
CREATE TYPE status_enum AS ENUM ('ativo','inativo','suspenso');

CREATE TYPE sexo_enum AS ENUM ('masculino','feminino','outro','nao_informado');

CREATE TYPE estado_civil_enum AS ENUM (
    'solteiro','casado','divorciado','viuvo',
    'uniao_estavel','separado','nao_informado'
);

CREATE TYPE tipo_local_enum AS ENUM (
    'pais','estado','municipio','bairro',
    'logradouro','regiao','area_geografica',
    'estabelecimento','local_generico'
);

CREATE TYPE tipo_contato_enum AS ENUM (
    'telefone','email','endereco','pessoa_relacionada'
);

CREATE TYPE classificacao_contato_enum AS ENUM (
    'residencial','comercial','outro'
);

CREATE TYPE papel_grupo_enum AS ENUM (
    'gestor','membro','associado','interessado'
);

-- =========================================
-- LOCAL
-- =========================================
CREATE TABLE local (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255) NOT NULL,
    tipo tipo_local_enum NOT NULL,
    id_local_pai BIGINT,
    FOREIGN KEY (id_local_pai) REFERENCES local(id)
);

CREATE INDEX idx_local_pai ON local(id_local_pai);

-- =========================================
-- PESSOA
-- =========================================
CREATE TABLE pessoa (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255) NOT NULL,
    tipo tipo_pessoa_enum NOT NULL,
    status status_enum NOT NULL DEFAULT 'ativo',
    observacao TEXT,

    criado_por BIGINT NOT NULL,
    criacao_em TIMESTAMPTZ NOT NULL DEFAULT now(),
    atualizado_por BIGINT NOT NULL,
    atualizado_em TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX idx_pessoa_tipo ON pessoa(tipo);
CREATE INDEX idx_pessoa_status ON pessoa(status);

-- =========================================
-- PROFISSAO
-- =========================================
CREATE TABLE profissao (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(255) NOT NULL
);

-- =========================================
-- PESSOA FISICA
-- =========================================
CREATE TABLE pessoa_fisica (
    id BIGINT PRIMARY KEY,
    cpf VARCHAR(11),
    rg VARCHAR(20),

    id_pessoa_mae BIGINT,
    id_pessoa_pai BIGINT,
    nome_mae VARCHAR(255),
    nome_pai VARCHAR(255),

    data_nascimento DATE,
    local_nascimento_nome VARCHAR(255),
    id_local_nascimento BIGINT,

    data_falecimento DATE,
    local_falecimento_nome VARCHAR(255),
    id_local_falecimento BIGINT,

    sexo sexo_enum,
    estado_civil estado_civil_enum,
    nacionalidade VARCHAR(100),

    id_profissao BIGINT,
    profissao_nome VARCHAR(255),

    FOREIGN KEY (id) REFERENCES pessoa(id) ON DELETE CASCADE,
    FOREIGN KEY (id_pessoa_mae) REFERENCES pessoa(id),
    FOREIGN KEY (id_pessoa_pai) REFERENCES pessoa(id),
    FOREIGN KEY (id_local_nascimento) REFERENCES local(id),
    FOREIGN KEY (id_local_falecimento) REFERENCES local(id),
    FOREIGN KEY (id_profissao) REFERENCES profissao(id)
);

CREATE UNIQUE INDEX uq_pf_cpf ON pessoa_fisica(cpf) WHERE cpf IS NOT NULL;

-- =========================================
-- PESSOA JURIDICA
-- =========================================
