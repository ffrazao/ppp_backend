-- =========================================
-- TRIGGERS DE INATIVACAO
-- =========================================
CREATE OR REPLACE FUNCTION trg_inativar_pf()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.local_falecimento_nome IS NOT NULL
       OR NEW.id_local_falecimento IS NOT NULL THEN
        UPDATE pessoa SET status = 'inativo' WHERE id = NEW.id;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_pf_inativo
AFTER INSERT OR UPDATE ON pessoa_fisica
FOR EACH ROW EXECUTE FUNCTION trg_inativar_pf();

CREATE OR REPLACE FUNCTION trg_inativar_pj()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.local_sede_nome IS NULL AND NEW.id_local_sede IS NULL THEN
        RETURN NEW;
    END IF;
    IF EXISTS (
        SELECT 1 FROM pessoa p WHERE p.id = NEW.id AND p.data_termino IS NOT NULL
    ) THEN
        UPDATE pessoa SET status = 'inativo' WHERE id = NEW.id;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_pj_inativo
AFTER INSERT OR UPDATE ON pessoa_juridica
FOR EACH ROW EXECUTE FUNCTION trg_inativar_pj();

CREATE OR REPLACE FUNCTION trg_inativar_grupo()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.data_termino IS NOT NULL THEN
        UPDATE pessoa SET status = 'inativo' WHERE id = NEW.id;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_grupo_inativo
AFTER INSERT OR UPDATE ON pessoa_grupo
FOR EACH ROW EXECUTE FUNCTION trg_inativar_grupo();

-- =========================================
-- VALIDACAO PAI/MAE
-- =========================================
CREATE OR REPLACE FUNCTION trg_validar_pais()
RETURNS TRIGGER AS $$
DECLARE sexo_mae sexo_enum;
DECLARE sexo_pai sexo_enum;
BEGIN
    IF NEW.id_pessoa_mae IS NOT NULL THEN
        SELECT sexo INTO sexo_mae FROM pessoa_fisica WHERE id = NEW.id_pessoa_mae;
        IF sexo_mae <> 'feminino' THEN
            RAISE EXCEPTION 'Mãe inválida';
        END IF;
    END IF;

    IF NEW.id_pessoa_pai IS NOT NULL THEN
        SELECT sexo INTO sexo_pai FROM pessoa_fisica WHERE id = NEW.id_pessoa_pai;
        IF sexo_pai <> 'masculino' THEN
            RAISE EXCEPTION 'Pai inválido';
        END IF;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_validar_pais
BEFORE INSERT OR UPDATE ON pessoa_fisica
FOR EACH ROW EXECUTE FUNCTION trg_validar_pais();

