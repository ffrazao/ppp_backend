-- HASH ENDERECO
CREATE OR REPLACE FUNCTION normalizar_texto(t TEXT)
RETURNS TEXT AS $$
BEGIN
    RETURN lower(trim(coalesce(t,'')));
END;
$$ LANGUAGE plpgsql IMMUTABLE;

CREATE OR REPLACE FUNCTION trg_hash_endereco()
RETURNS TRIGGER AS $$
BEGIN
    NEW.hash_endereco :=
        digest(
            normalizar_texto(NEW.cep) ||
            normalizar_texto(NEW.logradouro) ||
            normalizar_texto(NEW.numero) ||
            normalizar_texto(NEW.complemento) ||
            normalizar_texto(NEW.bairro) ||
            coalesce(NEW.id_local::text,''),
            'sha256'
        );
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_hash_endereco
BEFORE INSERT OR UPDATE ON contato_endereco
FOR EACH ROW EXECUTE FUNCTION trg_hash_endereco();

CREATE UNIQUE INDEX uq_endereco_hash ON contato_endereco(hash_endereco);

