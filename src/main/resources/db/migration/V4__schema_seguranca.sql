CREATE SCHEMA IF NOT EXISTS seguranca;
SET search_path TO seguranca;


-- seguranca.consentimento definition

-- Drop table

-- DROP TABLE seguranca.consentimento;

CREATE TABLE seguranca.consentimento (
	id int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	organizacao_id uuid NOT NULL,
	usuario_id varchar(64) NOT NULL,
	tipo_consentimento varchar(64) NOT NULL,
	status varchar(32) DEFAULT 'ATIVO'::character varying NOT NULL,
	criado_por varchar(64) NOT NULL,
	criado_em timestamp NOT NULL,
	atualizado_por varchar(64) NULL,
	atualizado_em timestamp NULL
);


