-- seguranca.identidade_acesso definition

-- Drop table

-- DROP TABLE seguranca.identidade_acesso;

CREATE TABLE seguranca.identidade_acesso (
    id int8 GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pessoa_id int8 NOT NULL,
    keycloak_sub varchar(64) NOT NULL,
    provedor varchar(32) DEFAULT 'keycloak' NOT NULL,
    criado_em timestamp DEFAULT now() NOT NULL,
    CONSTRAINT fk_identidade_pessoa FOREIGN KEY (pessoa_id) REFERENCES pessoa.pessoa(id) ON DELETE CASCADE,
    CONSTRAINT uq_identidade_sub UNIQUE (keycloak_sub)
);

-- Índice para otimizar as buscas no momento do login pelo interceptador de segurança
CREATE INDEX idx_identidade_acesso_sub ON seguranca.identidade_acesso USING btree (keycloak_sub);

